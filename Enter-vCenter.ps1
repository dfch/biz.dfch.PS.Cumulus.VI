function Enter-vCenter {
<#
.SYNOPSIS
Connects to a VMware vCenter server.


.DESCRIPTION
Connects to a VMware vCenter server.


.EXAMPLE
Enter-vCenter https://vcenter1

Connects to vCenter1 via https

.EXAMPLE
Enter-vCenter vcenter1

Connects to vCenter1 via https and tcp port 443 (implicit)

.EXAMPLE
Enter-vCenter -Server vcenter1 -Port 44300 -Scheme https

Connects to vCenter1 via https and tcp port 44300

.EXAMPLE
Enter-vCenter @('vcenter1', 'vcenter2')

Connects to the list of vCenters vcenter1 and vcenter2

.EXAMPLE
Enter-vCenter 'vcenter1','vcenter2'

Connects to the list of vCenters vcenter1 and vcenter2

.EXAMPLE
@('vcenter1', 'vcenter2') Enter-vCenter

Connects to the list of vCenters vcenter1 and vcenter2 via pipeline input

.EXAMPLE
'vcenter1','vcenter2' | Enter-vCenter

Connects to the list of vCenters vcenter1 and vcenter2 via pipeline input

.EXAMPLE
Enter-vCenter https://vcenter1 -Force

Will login to a vCenter even if a connection already exists (existing connection will be disconnecte first)

.EXAMPLE
Enter-vCenter vcenter1 -Credential $cred

Connects to vCenter1 with explicit Credentials ($cred)

.NOTES
See module manifest for required software versions and dependencies at:
http://dfch.biz/biz/dfch/PS/Cumulus/VI/biz.dfch.PS.Cumulus.VI.psd1/

Aliases 'vCenterNames' and 'vCenterCred' only left for compability. 
Should be removed at a future point in time.
#>
[CmdletBinding(
    SupportsShouldProcess = $true
	,
    ConfirmImpact = "Medium"
	,
	HelpURI = 'http://dfch.biz/biz/dfch/PS/Cumulus/VI/Enter-vCenter/'
	,
	DefaultParameterSetName = 'uri'
)]
PARAM
(
	# Specifies the vCenter names to connect to
	[ValidateScript( { foreach($Object in $_) { if( $Object -isnot [String] -And $Object -isnot [Uri] ) { throw($Object); }; return $true; }; } )]
	[Parameter(Mandatory = $true, Position = 0, ValueFromPipeline = $true, ParameterSetName = 'input')]
	[ValidateNotNullOrEmpty()]
	[Alias('vCenterNames')]
	$InputObject
	,
	# Specifies the vCenter names to connect to
	[Parameter(Mandatory = $true, Position = 0, ParameterSetName = 'uri')]
	[ValidateNotNullOrEmpty()]
	[Uri] $Uri
	,
	# Specifies the vCenter names to connect to
	[Parameter(Mandatory = $true, ParameterSetName = 'server')]
	[ValidateNotNullOrEmpty()]
	[String] $Server
	,
	# Specifies the vCenter port number to connect to
	[Parameter(Mandatory = $true, ParameterSetName = 'server')]
	[ValidateNotNullOrEmpty()]
	[int] $Port = 443
	,
	# Specifies the vCenter protocol scheme to connect to
	[Parameter(Mandatory = $true, ParameterSetName = 'server')]
	[ValidateSet('http', 'https')]
	[String] $Scheme = 'https'
	,
	# Specifies the credential set to make the connection to the vCenter
	[Parameter(Mandatory = $false, Position = 1)]
	[ValidateNotNullOrEmpty()]
	[Alias('vCenterCred')]
	[PSCredential] $Credential
	,
	# Specifies whether an existing conenction to the vCenter server should be overwritten (and disconnected first)
	[Parameter(Mandatory = $false)]
	[Switch] $Force = $false
)
BEGIN
{
	$datBegin = [datetime]::Now;
	[string] $fn = $MyInvocation.MyCommand.Name;
	Log-Debug -fn $fn -msg ("CALL. ParameterSetName '{0}'" -f $PSCmdlet.ParameterSetName) -fac 1;
	
	if('server' -eq $PSCmdlet.ParameterSetName)
	{
		$UriString = '{0}://{1}:{2}' -f $Scheme, $Server, $Port;
		if(![Uri]::TryCreate($UriString, [System.UriKind]::Absolute, [ref] $Uri))
		{
			$msg = "Scheme/Server/Port: Parameter validation FAILED."; 
			Log-Error $fn $msg;
			$e = New-CustomErrorRecord -m $msg -cat InvalidArgument -o $PSBoundParameters; 
			throw($gotoError); 
			$PSCmdlet.ThrowTerminatingError($e);
		}
	}
	if($Uri)
	{
		$InputObject = $Uri;
	}
}
# BEGIN

PROCESS
{

[Boolean] $fReturn = $false;
$OutputParameter = $null;

try
{
	foreach($Object in $InputObject)
	{
		if(!$PSCmdlet.ShouldProcess(($Object)))
		{
			continue;
		}
		if($Object -is [String])
		{
			[Uri] $LocalUri = $null;
			if(![Uri]::TryCreate($Object, [System.UriKind]::Absolute, [ref] $LocalUri))
			{
				$Object = '{0}://{1}:{2}' -f $Scheme, $Object, $Port;
				if(![Uri]::TryCreate($Object, [System.UriKind]::Absolute, [ref] $LocalUri))
				{
					$msg = "Object: Parameter validation FAILED."; 
					Log-Error $fn $msg;
					$e = New-CustomErrorRecord -m $msg -cat InvalidArgument -o $Object; 
					throw($gotoError); 
					$PSCmdlet.ThrowTerminatingError($e);
				}
			}
			$Object = $LocalUri;
		}
		
		foreach($VIServer in $global:DefaultVIServers)
		{
			# this check will produce a 'false negative' if host only and FQDN are compared
			[Uri] $UriVIServer = $VIServer.ServiceUri;
			if($Object.AbsoluteUri -eq $UriVIServer.AbsoluteUri)
			{
				if(!$Force)
				{
					Log-Warn $fn ("ViServer '{0}' == '{1}' is already connected." -f $Object.AbsoluteUri, $ViServer.ServiceUri);
					continue;
				}
				Log-Info $fn ("Disconnecting from already connected ViServer '{0}' (Force) ..." -f $ViServer.ServiceUri);
				$null = Disconnect-VIServer -Server $VIServer -Confirm:$false -Force -ErrorAction:SilentlyContinue;
				break;
			}
		}
		Log-Debug $fn ("Connecting to ViServer '{0}' ..." -f $Object.AbsoluteUri);
		if($Credential)
		{
			$ViServerConnection = Connect-VIServer -Server $Object.Host -Port $Object.Port -Protocol $Object.Scheme -Credential $Credential -ErrorAction:SilentlyContinue -Force;
		}
		else
		{
			$ViServerConnection = Connect-VIServer -Server $Object.Host -Port $Object.Port -Protocol $Object.Scheme -ErrorAction:SilentlyContinue -Force;
		}
		if(!$ViServerConnection)
		{
			Log-Error $fn ("Connecting to ViServer '{0}' FAILED." -f $Object.AbsoluteUri);
			continue;
		}
		$OutputParameter += $ViServerConnection;
		Log-Info $fn ("Connecting to ViServer '{0}' SUCCEEDED." -f $Object.AbsoluteUri);
	}
}
catch 
{
	if($gotoSuccess -eq $_.Exception.Message) 
	{
		$fReturn = $true;
	} 
	else 
	{
		[string] $ErrorText = "catch [$($_.FullyQualifiedErrorId)]";
		$ErrorText += (($_ | fl * -Force) | Out-String);
		$ErrorText += (($_.Exception | fl * -Force) | Out-String);
		$ErrorText += (Get-PSCallStack | Out-String);
		
		if($_.Exception -is [System.Net.WebException]) 
		{
			Log-Critical $fn ("[WebException] Request FAILED with Status '{0}'. [{1}]." -f $_.Status, $_);
			Log-Debug $fn $ErrorText -fac 3;
		}
		else 
		{
			Log-Error $fn $ErrorText -fac 3;
			if($gotoError -eq $_.Exception.Message) 
			{
				Log-Error $fn $e.Exception.Message;
				$PSCmdlet.ThrowTerminatingError($e);
			} 
			elseif($gotoFailure -ne $_.Exception.Message) 
			{ 
				Write-Verbose ("$fn`n$ErrorText"); 
			} 
			else 
			{
				# N/A
			}
		}
		$fReturn = $false;
		$OutputParameter = $null;
	}
}
finally 
{
	# Clean up
	# N/A
}

}
# PROCESS

END 
{

$datEnd = [datetime]::Now;
Log-Debug -fn $fn -msg ("RET. fReturn: [{0}]. Execution time: [{1}]ms. Started: [{2}]." -f $fReturn, ($datEnd - $datBegin).TotalMilliseconds, $datBegin.ToString('yyyy-MM-dd HH:mm:ss.fffzzz')) -fac 2;

# Return values are always and only returned via OutputParameter.
return $OutputParameter;

}
# END

} # function

if($MyInvocation.ScriptName) { Export-ModuleMember -Function Enter-vCenter; } 


